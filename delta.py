"""
Задача для GigaCgat: 
Дан датафрейм df1, в котором есть столбец Time_event. 
В этом столбце указано время события. Создай код на Python, который добавит столбец Duration, 
в котором указывается разница во времени между соседникми строками. 
В самом первом столбце Duration = 0.
"""

import pandas as pd

# Предположим, что у нас есть следующий датафрейм df1:
df1 = pd.DataFrame({'Time_event': ['2022-01-01 09:00', '2022-01-01 09:30', '2022-01-01 10:10']})

# Преобразуем столбец Time_event в формат datetime
df1['Time_event'] = pd.to_datetime(df1['Time_event'])

# Добавляем столбец Duration, который будет содержать разницу во времени между соседними строками
df1['Duration'] = df1['Time_event'].diff().fillna(pd.Timedelta(0))

# Выводим результат
print(df1)

"""
Метод fillna используется для заполнения отсутствующих значений в столбце Duration. 
Это необходимо, потому что метод diff может привести к тому, что 
некоторые значения будут отсутствовать, если между двумя строками нет разницы во времени. 
Например, если два события произошли одновременно, разница во времени будет равна нулю, 
и в результате применения diff получится NaN (Not a Number).

Метод fillna позволяет заполнить эти отсутствующие значения, чтобы все значения в
столбце Duration были представлены в виде pandas.Timedelta. 
В данном случае мы используем pd.Timedelta(0) для заполнения отсутствующих 
значений нулевой продолжительностью.
"""